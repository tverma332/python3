import sys 

# 1) version = Gives Version of python runtime

print(sys.version)

# 2) modules = Returns all the modules python are importing to run your python scripts

print(sys.modules)

# 3) path = Returns list of path of libraries your python script need to run

print(sys.path)

# 4) argv = Returns a list of command line arguments passed to the python script

print(sys.argv)

