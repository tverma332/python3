# 1) factorial() = we can find the factorial of a number in a single line of the code

import math 
print(math.factorial(3))

# 2) ceil() = Ceil value means the smallest integral value greater than the number

import math 
print(math.ceil(3.09))

# 3) floor() = floor value means the greatest integral value smaller than the number

import math 
print(math.floor(3.09))

# 4) pow() = pow() function computes x**y. This function first converts its arguments into float and then computes the power

import math 
print(math.pow(3,5))

