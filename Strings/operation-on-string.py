# 1) swapcase = Convert uppercase characters to lowercase and lowercase characters to uppercase

x="python"
print(x.swapcase())

# 2) casefold = Convert in lowercase

print(x.casefold())

# 3) capitalize = More specifically, make the first character have upper case and the rest lower case

print(x.capitalize())

# 4) title = More specifically, words start with uppercased characters and all remaining cased characters have lower case
 
print(x.title())
