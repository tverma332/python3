# 1) keys = You'll get only dictionary keys

my_dict={"fruit":"apple","animal":"fox",1:"one","two":2}

print(my_dict.keys())

# 2) values = Get only values from dictionary

my_dict={"fruit":"apple","animal":"fox",1:"one","two":2}

print(my_dict.values())

# 3) items = You'll get keys & values as 1 items | Gives list of tuple pair

my_dict={"fruit":"apple","animal":"fox",1:"one","two":2}

print(my_dict.items())