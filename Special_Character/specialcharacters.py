# Usage of Special characters
# Note: Use Special chars. only in quotes


# 1) \n = Next line(Everything written after \n will be print on Next line)

print("Hello\nWorld")

# 2) \b = Back Space

print("Hello \b\bworld")

# 3) \t = tab(Add tab space)

print("Hello\tworld")

# 4) \ = Escape symbol(useful in case of defining paths mainly windows path)

print('Hello this is python\'s class') 